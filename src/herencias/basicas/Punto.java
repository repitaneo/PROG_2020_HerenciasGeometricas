package herencias.basicas;

public abstract class Punto {

	protected int x;
	protected int y;

	
	protected static final float CALCULO_IMPOSIBLE = -5000;
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

	
	
	
	
}
